package com.taotieshop.demo.entity;

public class Category {
    private Integer id;

    private String name;

    private String keywords;

    private String front_desc;

    private Integer parent_id;

    private Boolean sort_order;

    private Boolean show_index;

    private Boolean is_show;

    private String banner_url;

    private String icon_url;

    private String img_url;

    private String wap_banner_url;

    private String level;

    private Integer type;

    private String front_name;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getKeywords() {
        return keywords;
    }

    public void setKeywords(String keywords) {
        this.keywords = keywords;
    }

    public String getFront_desc() {
        return front_desc;
    }

    public void setFront_desc(String front_desc) {
        this.front_desc = front_desc;
    }

    public Integer getParent_id() {
        return parent_id;
    }

    public void setParent_id(Integer parent_id) {
        this.parent_id = parent_id;
    }

    public Boolean getSort_order() {
        return sort_order;
    }

    public void setSort_order(Boolean sort_order) {
        this.sort_order = sort_order;
    }

    public Boolean getShow_index() {
        return show_index;
    }

    public void setShow_index(Boolean show_index) {
        this.show_index = show_index;
    }

    public Boolean getIs_show() {
        return is_show;
    }

    public void setIs_show(Boolean is_show) {
        this.is_show = is_show;
    }

    public String getBanner_url() {
        return banner_url;
    }

    public void setBanner_url(String banner_url) {
        this.banner_url = banner_url;
    }

    public String getIcon_url() {
        return icon_url;
    }

    public void setIcon_url(String icon_url) {
        this.icon_url = icon_url;
    }

    public String getImg_url() {
        return img_url;
    }

    public void setImg_url(String img_url) {
        this.img_url = img_url;
    }

    public String getWap_banner_url() {
        return wap_banner_url;
    }

    public void setWap_banner_url(String wap_banner_url) {
        this.wap_banner_url = wap_banner_url;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getFront_name() {
        return front_name;
    }

    public void setFront_name(String front_name) {
        this.front_name = front_name;
    }
}